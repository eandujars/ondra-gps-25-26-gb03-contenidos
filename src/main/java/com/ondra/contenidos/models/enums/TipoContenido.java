package com.ondra.contenidos.models.enums;

/**
 * Enumeración que representa el tipo de contenido multimedia.
 *
 * <p>Utilizado para diferenciar entre canciones y álbumes en entidades
 * como Favorito, Compra, Comentario y Valoracion.</p>
 */
public enum TipoContenido {
    /**
     * Contenido de tipo canción individual.
     */
    CANCIÓN,

    /**
     * Contenido de tipo álbum musical.
     */
    ÁLBUM
}